Definition:
CREATE DATABASE command is a DDL (Data Definition Language) statement in SQL that is used to create a new database in the database server.
CREATE DATABASE is an SQL command used to define and create a new database where data will be stored, organized, and managed.
- The CREATE DATABASE command is specifically used to create a single new database.
- SQL does not allow creating multiple databases within one CREATE DATABASE statement.
- If the database name includes spaces, it must be enclosed in double quotes (" ").
- Both forms of the command (with or without quotes) are valid, but using names without spaces is 
  considered more professional and is the preferred style among developers and big tech companies.


Syntax: 
CREATE DATABASE database_name;
